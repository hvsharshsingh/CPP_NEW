// NUMBER SQUARE
# include <iostream>
using namespace std;
int main (){
    int m, n;
    cout<<"Enter the number of ROWS : ";
    cin>>m;
    cout<<"Enter the number of COLUMNS : ";
    cin>>n;
    for (int i=1;i<=m;i++){
        for (int j=1;j<=n;j++){
            cout<<"1234";
        }
        cout<<endl;
    }
    return 0;
}